public class rep61 {
    public static void main(String[] args) {
        int multiplier = 3;

        // Use a for loop to print the multiplication of 3 from 1 to 10
        for (int i = 1; i <= 10; i++) {
            int result = multiplier * i;
            System.out.println(multiplier + " * " + i + " = " + result);
        }


    }
}
