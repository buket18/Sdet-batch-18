package review4;

public class ForLoop2 {
    public static void main(String[] args) {
        for(int i=2; i<=10; i+=3){
            System.out.print(i +" ");
        }
   //we want to print numbers from 10 till 2
        for(int i=10; i>=2; i--){
            System.out.print(i +" ");
        }



    }



}
