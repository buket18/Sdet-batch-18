package class7;

public class E1nestedLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.print("*");
        }
        System.out.println();
        for (int i = 0; i < 5; i++) {
            System.out.print("*");
        }
        System.out.println();
        for (int i = 0; i < 5; i++) {
            System.out.print("*");
        }
    }
}
